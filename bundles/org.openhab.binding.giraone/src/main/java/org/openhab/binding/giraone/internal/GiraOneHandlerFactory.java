package org.openhab.binding.giraone.internal;

import static org.openhab.binding.giraone.internal.GiraOneBindingConstants.*;

import org.eclipse.jdt.annotation.NonNullByDefault;
import org.eclipse.jdt.annotation.Nullable;
import org.openhab.core.thing.Bridge;
import org.openhab.core.thing.Thing;
import org.openhab.core.thing.ThingTypeUID;
import org.openhab.core.thing.binding.BaseThingHandlerFactory;
import org.openhab.core.thing.binding.ThingHandler;
import org.openhab.core.thing.binding.ThingHandlerFactory;
import org.osgi.service.component.annotations.Component;

/**
 * The {@link GiraOneHandlerFactory} is responsible for creating things and thing
 * handlers.
 *
 * @author Matthias Groeger - Initial contribution
 */
@NonNullByDefault
@Component(configurationPid = "binding.giraone", service = ThingHandlerFactory.class)
public class GiraOneHandlerFactory extends BaseThingHandlerFactory {

    @Override
    public boolean supportsThingType(ThingTypeUID thingTypeUID) {
        return SUPPORTED_THING_TYPES_UIDS.contains(thingTypeUID);
    }

    @Override
    protected @Nullable ThingHandler createHandler(Thing thing) {
        ThingTypeUID thingTypeUID = thing.getThingTypeUID();

        if (BRIDGE_TYPE_UID.equals(thingTypeUID)) {

            org.openhab.binding.giraone.internal.GiraOneBridgeHandler bridgeHandler = new org.openhab.binding.giraone.internal.GiraOneBridgeHandler(
                    (Bridge) thing);

            return bridgeHandler;
        }

        return null;
    }
}
